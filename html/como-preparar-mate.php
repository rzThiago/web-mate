        <div class="informacion-principal">
			<div class="informacion-principal-titulo">
				<h1>Como Preparar un Mate</h1>
			</div>
            <p>
				Para preparar un mate no se necesita más que un recipiente (mate), yerba mate, una bombilla y agua caliente; pero para preparar un “buen mate” hay que conocer algunos trucos que ayudaran a intensificar su sabor y mantenerlo por más tiempo.
			</p>
			<br>
			<h2>Calentar el agua</h2>
			<p> 
				Caliente el agua, tenga en cuenta que el agua no debe estar tibia ni dejarse hervir, para tomar “buenos mates” la temperatura del agua aconsejable es  de 70 a 80°C. Se aconseja verter el agua caliente en un termo para que dure más tiempo.
			</p>
			<br>
			<div class="informacion-principal-imagen">
				<img src="/imagenes/pava.png">
			</div>
			<br>
			<h2>Arrojar yerba en el mate</h2>
			<p>
				Mientras el agua se este calentando vierta, yerba dentro del mate hasta alcanzar las tres cuartas partes del mismo. Si lo desea, para que los primeros mates no sean tan amargos, puede agregar  una o dos cucharaditas de azúcar.
			</p>
			<br>
			<div class="informacion-principal-imagen">
				<img src="/imagenes/yerba-vertiendose.jpg">
			</div>
			<br>
			<h2>Agite el mate</h2>
			<p>
				Tape con una mano la boca del mate, inviértalo y agítelo unos instantes. Vuelva el objeto a su posición normal. Cuide que la yerba quede recostada sobre una de las paredes del mate y además se forme un pequeño agujero.
			</p>
			<br>
			<div class="informacion-principal-imagen">
				<img src="/imagenes/mate-agitandose.png">
			</div>
			<br>
			<h2>Derramar agua tibia</h2>
			<p>
				Para comenzar el mate, coloque agua tibia suavemente en el hueco que quedó en la yerba. Iniciar el mate con agua tibia, permitirá que no se queme la yerba y pierda el gusto.  Deje reposar unos instantes.
			</p>
			<br>
			<div class="informacion-principal-imagen">
				<img src="/imagenes/agua-tibia.jpg">
			</div>
			<br>
			<h2>Introducir la bombilla</h2>
			<p>
				Introduzca la bombilla en el hueco húmedo. Una recomendación práctica: 
				tapá con un dedo el orificio superior de la bombilla y colocala en el hueco. 
				El gran riesgo de este paso es que el mate quede tapado.  
			</p>
			<br>
			<div class="informacion-principal-imagen">
				<img src="/imagenes/bombilla-colocandose.jpg">
			</div>
			<br>
			<h2>Cebar</h2>
			<p>
				Ahora sí con la temperatura ideal antes mencionada, empezamos a cebar el mate. 
				Se debe colocar poco agua por turno. Procure no mover la bombilla y cebar siempre en el mismo lugar, esto permitirá que no moje toda la yerba y así disfrutará más tiempo de unos exquisitos mates.
				<br>
				Importante: el cebador siempre es el mismo.
			</p>
			<br>
			<div class="informacion-principal-imagen">
