            <h2>Con Naranja o Limon</h2>
            <p>
                Que el mate produce acidez no es ningún secreto. Algunos materos agregan cáscaras de naranja o de limón para contrarrestar el ardor de estómago y, de paso, darle un toque diferente a la infusión. 
                Antes hay que secar las cáscaras, calentarlas a fuego directo, picarlas y después mezclar con la yerba. 
            </p>
            <br>
            <div class="informacion-principal-imagen">
                <img src="/imagenes/mate-limon.png">
