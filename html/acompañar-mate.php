            <h2>Pastafrola</h2>
            <h4>Ingredientes:</h4>
            <ul>
                <li>150g de manteca</li>
                <li>150g de azúcar</li>
                <li>2 huevos</li>
                <li>300g de harina</li>
                <li>Ralladura de limón</li>
                <li>1 cuchadarita de polvo para hornear</li>
                <li>1 cucharadita de esencia de vainilla</li>
                <li>600g de membrillo o batata</li>
            </ul>
            <h4>Preparación:</h4>
            <ul>
                <li>1.Mezclar en un bol el azúcar con la manteca a temperatura ambiente. Luego incorporar los huevos uno a uno,
                    la escancia de vainilla y la ralladura de limon.
                </li>
                <li>2.Tamizar la harina y mezclarla con el polvo para hornear. Unir los ingredientes hasta conseguir una masa blanda.</li>
                <li>3.Estirar la masa y dejarla reposar en la heladera por 30 minutos.</li>
                <li>4.Pasado el tiempo, retirar la masa hasta que tome temperatura ambiente. Mientras engrasar y espolvorear con harina 
                    un molde para horno. Luego, estirar la masa con los dedos hasta cubrir el molde formando una base.
                </li>
                <li>5.Cortar el dulce de membrillo o batata en pequeños trozos, y aplastarlo con ayuda de agua caliente hasta que se ablande todo</li>
                <li>6.Agrefar el dulce sobre la masa y esparcelo bien por todas partes. Cubrir este dulce con tiras de masa sobrante para decorar</li>
                <li>7.Llevar el molde al horno con una temperatura de 180°C por 45 minutos. Luego retirar y dejar reposar por unos minutos.</li>
            </ul>
