            <h2>Torta Frita</h2>
            <h4>Ingredientes:</h4>
            <ul>
                <li>1/2Kg de harina</li>
                <li>4 cucharadas de manteca o grasa</li>
                <li>1/2 taza de agua</li>
                <li>2 cucharaditas de sal</li>
                <li>Aceite o grasa para freir</li>
            </ul>
            <h4>Preparación:</h4>
            <ul>
                <li>1.Colocar la harina en un bol. Hacer un hueco en la harina y coloquen la grasa o manteca a temperatura ambiente.</li>
                <li>2.Amasar mientras se agrega el agua y la sal poco a poco hasta formar un bollo</li>
                <li>3.Dejar reposar la masa por lo menos una hora. Luego formar pequeños bollos, achatarlas con las manos y pincharlas</li>
                <li>4.Freir en abundante aceite o grasa caliente. Luego retirarlas y dejarlas reposar sobre papel absorbente.</li>
            </ul>
