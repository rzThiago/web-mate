                <h2>Chipá</h2>
                <h4>Ingredientes:</h4>
                <ul>
                    <li>500g de almidón de mandioca</li>
                    <li>2 huevos</li>
                    <li>55cc de leche</li>
                    <li>100g de manteca</li>
                    <li>300g de queso semiduro</li>
                    <li>Sal</li>
                </ul>
                <h4>Preparación:</h4>
                <ul>
                    <li>1.En un bol colocar el almidón de mandioca y hacer un hueco en el medio, colocar los huevos uno por uno, manteca en cubos y sal a gusto</li>
                    <li>2.Amasar hasta que los ingredientes se integren. Luego agregar el queso semiduro en cubos a la mezcla, seguir amasando y agregar leche</li>
                    <li>3.Cortar la masa, forma bollos pequeños y agregalas a una placa de horno untada con manteca</li>
                    <li>4.Colocar la placa al horno a fuego fuerte por no más de 20 minutos</li>
                    <li>5.Retirarlas y dejarlas reposar 5 minutos.</li>
                </ul>
