            <h2>Bizcochitos con grasa</h2>
            <h4>Ingredientes:</h4>
            <ul>
                <li>500g de harina 0000</li>
                <li>20g de levadura fresca</li>
                <li>1/2 cucharada de sal</li>
                <li>250ml de agua</li>
                <li>150g de grasa</li>
            </ul>
            <h4>Preparación:</h4>
            <ul>
                <li>1.Colocar sobre la mesa en forma de corona, en el centro colocar la grasa y la levadura, por los costados colocar la sal 
                    y mezclar los ingredientes hasta conseguir una masa uniforme. Dejarla reposar por 30 minutos</li>
                    <li>2.Estirar la mas de manera rectangular y realizar un pliegue y dejar de descansar por 20 minutos</li>
                    <li>3.Utilizar un cortante circular y pinchar con un tenedor en el centro. Realizar lo mismo con la masa sobrante</li>
                    <li>4.Colocar los bizcochos sobre un placa enharinada y cocinarlas en un horno a 180°C por 20 minutos</li>
                    <li>5.Dejarlas reposar por unos 10 minutos</li>
                </ul>
